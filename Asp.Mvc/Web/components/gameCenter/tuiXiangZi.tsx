import "./styles/tuiXiangZi.less";

import * as React from "react";

class Point {
  x: number = 0; y: number = 0;
  constructor(x, y) {
    this.x = x;
    this.y = y;
  }
}
export interface TuiXiangZiProps {
}
export interface TuiXiangZiState {
}
export class TuiXiangZi extends React.Component<TuiXiangZiProps, TuiXiangZiState>{
  cxt: any;
  w: number = 35; h: number = 35;
  curMap: any;//当前的地图
  curLevel: any;//当前等级的地图
  curMan: any;//初始化小人
  iCurlevel: number = 0;//关卡数
  moveTimes: number = 0;//移动了多少次
  oImgs = {//所有图片
    block: require<string>("./imgs/tuiXiangZi/block.gif"),
    wall: require<string>("./imgs/tuiXiangZi/wall.png"),
    box: require<string>("./imgs/tuiXiangZi/box.png"),
    okbox: require<string>("./imgs/tuiXiangZi/okbox.png"),
    ball: require<string>("./imgs/tuiXiangZi/ball.png"),
    up: require<string>("./imgs/tuiXiangZi/up.png"),
    down: require<string>("./imgs/tuiXiangZi/down.png"),
    left: require<string>("./imgs/tuiXiangZi/left.png"),
    right: require<string>("./imgs/tuiXiangZi/right.png")
  };
  //Image资源对象
  block: any; wall: any; box: any; okbox: any;ball: any;
  up: any; down: any; left: any; right: any;
  perPosition: Point = new Point(0, 0);// 小人位置

  constructor() {
    super();
  }

  componentDidMount() {
    this.cxt = this.refs.canvas.getContext("2d");//画布
    this.curMap = this.copyArray(levels[this.iCurlevel]);//当前移动过的游戏地图
    this.curLevel = levels[this.iCurlevel];//当前等级的初始地图
    this.curMan = this.down;//初始化小人
    let count = 0, images = {};
    for (let img in this.oImgs) {
      images[img] = new Image();
      images[img].src = this.oImgs[img];
    }
    this.block = images['block'];
    this.wall = images['wall'];
    this.box = images['box'];
    this.okbox = images['okbox'];
    this.ball = images['ball'];
    this.up = images['up'];
    this.down = images['down'];
    this.left = images['left'];
    this.right = images['right'];
    this.init();
    document.onkeydown = this.doKeyDown.bind(this);
  }

  //初始化游戏
  private init() {
    this.initLevel();//初始化对应等级的游戏
    this.showMoveInfo();//初始化对应等级的游戏数据
  }
  //初始化游戏等级
  private initLevel() {
    this.curMap = this.copyArray(levels[this.iCurlevel]);//当前移动过的游戏地图
    this.curLevel = levels[this.iCurlevel];//当前等级的初始地图
    this.curMan = this.down;//初始化小人
    this.InitMap();//初始化地板
    this.DrawMap(this.curMap);//绘制出当前等级的地图
  }
  //绘制地板
  private InitMap() {
    for (var i = 0; i < 16; i++) {
      for (var j = 0; j < 16; j++) {
        this.cxt.drawImage(this.block, this.w * j, this.h * i, this.w, this.h);
      }
    }
  }
  //绘制每个游戏关卡地图
  private DrawMap(level) {
    for (var i = 0; i < level.length; i++) {
      for (var j = 0; j < level[i].length; j++) {
        var pic = this.block;//初始图片
        switch (level[i][j]) {
          case 1://绘制墙壁
            pic = this.wall;
            break;
          case 2://绘制陷进
            pic = this.ball;
            break;
          case 3://绘制箱子
            pic = this.box;
            break;
          case 4://绘制小人
            pic = this.curMan;//小人有四个方向 具体显示哪个图片需要和上下左右方位值关联
            //获取小人的坐标位置
            this.perPosition.x = i;
            this.perPosition.y = j;
            break;
          case 5://绘制箱子及陷进位置
            pic = this.okbox;
            break;
        }
        //每个图片不一样宽 需要在对应地板的中心绘制地图
        this.cxt.drawImage(pic, this.w * j - (pic.width - this.w) / 2, this.h * i - (pic.height - this.h), pic.width, pic.height)
      }
    }
  }
  //克隆二维数组
  private copyArray(arr) {
    var b = [];//每次移动更新地图数据都先清空再添加新的地图
    for (var i = 0; i < arr.length; i++) {
      b[i] = arr[i].concat();//链接两个数组
    }
    return b;
  }
  private NextLevel(i) {
    //iCurlevel当前的地图关数
    this.iCurlevel = this.iCurlevel + i;
    if (this.iCurlevel < 0) {
      this.iCurlevel = 0;
      return;
    }
    var len = levels.length;
    if (this.iCurlevel > len - 1) {
      this.iCurlevel = len - 1;
    }
    this.initLevel();//初始当前等级关卡
    this.moveTimes = 0;//游戏关卡移动步数清零
    this.showMoveInfo();//初始化当前关卡数据
  }
  //小人移动
  private go(dir) {
    var p1: Point = new Point(0, 0), p2: Point = new Point(0, 0);
    switch (dir) {
      case "up":
        this.curMan = this.up;
        //获取小人前面的两个坐标位置来进行判断小人是否能够移动
        p1.x = this.perPosition.x - 1;
        p1.y = this.perPosition.y;
        p2.x = this.perPosition.x - 2;
        p2.y = this.perPosition.y;
        break;
      case "down":
        this.curMan = this.down;
        p1.x = this.perPosition.x + 1;
        p1.y = this.perPosition.y;
        p2.x = this.perPosition.x + 2;
        p2.y = this.perPosition.y;
        break;
      case "left":
        this.curMan = this.left;
        p1.x = this.perPosition.x;
        p1.y = this.perPosition.y - 1;
        p2.x = this.perPosition.x;
        p2.y = this.perPosition.y - 2;
        break;
      case "right":
        this.curMan = this.right;
        p1.x = this.perPosition.x;
        p1.y = this.perPosition.y + 1;
        p2.x = this.perPosition.x;
        p2.y = this.perPosition.y + 2;
        break;
    }
    //若果小人能够移动的话，更新游戏数据，并重绘地图
    if (this.Trygo(p1, p2)) {
      this.moveTimes++;
      this.showMoveInfo();
    }
    //重绘地板
    this.InitMap();
    //重绘当前更新了数据的地图
    this.DrawMap(this.curMap);
    //若果移动完成了进入下一关
    if (this.checkFinish()) {
      alert("恭喜过关！！");
      this.NextLevel(1);
    }
  }
  //判断是否推成功
  private checkFinish() {
    for (var i = 0; i < this.curMap.length; i++) {
      for (var j = 0; j < this.curMap[i].length; j++) {
        //当前移动过的地图和初始地图进行比较，若果初始地图上的陷进参数在移动之后不是箱子的话就指代没推成功
        if (this.curLevel[i][j] == 2 && this.curMap[i][j] != 3 || this.curLevel[i][j] == 5 && this.curMap[i][j] != 3) {
          return false;
        }
      }
    }
    return true;
  }
  //判断小人是否能够移动
  private Trygo(p1: Point, p2: Point) {
    if (p1.x < 0) return false;//若果超出地图的上边，不通过
    if (p1.y < 0) return false;//若果超出地图的左边，不通过
    if (p1.x > this.curMap.length) return false;//若果超出地图的下边，不通过
    if (p1.y > this.curMap[0].length) return false;//若果超出地图的右边，不通过
    if (this.curMap[p1.x][p1.y] == 1) return false;//若果前面是墙，不通过
    if (this.curMap[p1.x][p1.y] == 3 || this.curMap[p1.x][p1.y] == 5) {//若果小人前面是箱子那就还需要判断箱子前面有没有障碍物(箱子/墙)
      if (this.curMap[p2.x][p2.y] == 1 || this.curMap[p2.x][p2.y] == 3) {
        return false;
      }
      //若果判断不成功小人前面的箱子前进一步
      this.curMap[p2.x][p2.y] = 3;//更改地图对应坐标点的值
      //console.log(curMap[p2.x][p2.y]);
    }
    //若果都没判断成功小人前进一步
    this.curMap[p1.x][p1.y] = 4;//更改地图对应坐标点的值
    //若果小人前进了一步，小人原来的位置如何显示
    var v = this.curLevel[this.perPosition.x][this.perPosition.y];
    if (v != 2)//若果刚开始小人位置不是陷进的话
    {
      if (v == 5)//可能是5 既有箱子又陷进
      {
        v = 2;//若果小人本身就在陷进里面的话移开之后还是显示陷进
      } else {
        v = 0;//小人移开之后之前小人的位置改为地板
      }
    }
    //重置小人位置的地图参数
    this.curMap[this.perPosition.x][this.perPosition.y] = v;
    //若果判断小人前进了一步，更新坐标值
    this.perPosition = p1;
    //若果小动了 返回true 指代能够移动小人
    return true;
  }
  //判断是否移动成功，与键盘上的上下左右键关联
  private doKeyDown(event) {
    switch (event.keyCode) {
      case 37://左键头
        this.go("left");
        break;
      case 38://上键头
        this.go("up");
        break;
      case 39://右箭头
        this.go("right");
        break;
      case 40://下箭头
        this.go("down");
        break;
    }
  }
  //完善关卡数据及游戏说明
  private showMoveInfo() {
    this.refs.msg.innerHTML = "第" + (this.iCurlevel + 1) + "关 移动次数: " + this.moveTimes;
  }
  //游戏说明
  private ShowHelp() {
    alert("用键盘上的上、下、左、右键移动小人，把箱子全部推到小球的位置即可过关。\n"+"箱子只可向前推，不能往后拉，并且小人一次只能推动一个箱子。");
  }
  refs: {
    [key: string]: any;
    canvas: HTMLCanvasElement;
    msg: HTMLDivElement;
  }

  render() {
    return (
      <div style={{ width: '100%', height: '100%', display: 'inline-flex', overflow: 'hidden' }} >
        <div className="tuiXiangZi" id="tuiXiangZi">
          <canvas ref="canvas" id="tuiXiangZi-canvas" width="560" height="560" style={{ float: 'left' }}></canvas>
          <div style={{ textAlign: 'center', float: 'right', marginTop: 200 }}>
            <div ref="msg" id="tuiXiangZi-msg">第1关&nbsp; 移动次数：0</div>
            <div><input type="button" value="上一关" onClick={this.NextLevel.bind(this, -1) }/></div>
            <div><input type="button" value="下一关" onClick={this.NextLevel.bind(this, 1) }/></div>
            <div><input type="button" value="重玩本关" onClick={this.NextLevel.bind(this, 0) }/></div>
            <div><input type="button" value="游戏说明" onClick={this.ShowHelp.bind(this) }/></div>
          </div>
        </div>
      </div>
    );
  }

}

